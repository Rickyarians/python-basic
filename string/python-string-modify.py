# Uppercase
a = "Ricky Ariansyah"
print(a.upper())

# lowercase
b = "Ricky Ariansyah"
print(b.lower())

# remove White Space
d = " Ricky Ariansyah "
print(d)
print(d.strip())

# cek spasi
c = " "
print(c.isspace())

# cek karakter terakhir
e = "Ricky Ariansyah"
print(e.endswith("a"))

f = "Ricky Ariansyah"
print(f.replace("Ariansyah", "Ganteng"))

g = "Ricky Ariansyah"
print(g.split(" ")[0])
print(g.split(" ")[1])

# Concat String
a1 = "Ricky"
a2 = "Ariansyah"
a3 = a1 + a2
print(a3)
print(a1 + a2)