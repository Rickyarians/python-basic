# Escape character
# ketika kita menyisipkan karakter tertentu sepert quote dll

# quote "
txt = "We are the so-called \"Vikings\" from the north."
print(txt)

# quote '
txt = "We are the so-called \'Vikings\' from the north."
print(txt)

# quote backslash
txt = "We are the so-called \\Vikings\\ from the north."
print(txt)

# quote new line
txt = "We are the so-called \nVikings\n from the north."
print(txt)

# quote new line
txt = "We are the so-called \rVikings from the north."
print(txt)

